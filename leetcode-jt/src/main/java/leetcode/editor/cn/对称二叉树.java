public class TreeNode {
	int val;
	TreeNode left;
	TreeNode right;
	TreeNode() {}
	TreeNode(int val) { this.val = val; }
	TreeNode(int val, TreeNode left, TreeNode right) {
		this.val = val;
		this.left = left;
		this.right = right;
	}
}
class Solution {
    public boolean isSymmetric(TreeNode root) {
		return checkSameTree(root.left,root.right);
    }
    public boolean checkSameTree(TreeNode left,TreeNode right){
    	if (left == null && right != null){
    		return false;
		}
    	if (left != null && right == null){
    		return false;
		}
    	if (left == null){
    		return true;
		}
    	return (left.val == right.val) && checkSameTree(left.left,right.right) && checkSameTree(left.right,right.left);
	}

}
